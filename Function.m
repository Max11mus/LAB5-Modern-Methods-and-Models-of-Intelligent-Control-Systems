% Побудова графіка функції y = x1^2*cos(x2-1) в межах x1 ∈ є [-5, 5] 
% і є[-3.5, 1.2].
n = 15; % кількість точок
x1 = linspace(-5, 5, n); % задання параметрів змінної x1
x2 = linspace(-3.5, 1.2, n); % задання параметрів змінної x2
y = zeros(n, n); % формування нульового масиву
% розміром n×n для вихідної змінної
for j = 1:n
    y(j, :) = x1.^2 .* cos(x2(j) - 1);
end
surf(x1, x2, y); % зображення поверхні функції
xlabel('x1');
ylabel('x2');
zlabel('y');
title('Target');
